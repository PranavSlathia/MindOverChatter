function isTabActive(tabPath: string, currentPath: string): boolean {
  if (tabPath === "/") return currentPath === "/";
  return currentPath.startsWith(tabPath);
}

export function BottomTabBar() {
  const location = useLocation();

  return (
    <nav className="border-t border-foreground/10 bg-background" aria-label="Main navigation">
      <div className="mx-auto flex max-w-lg items-center justify-around">
        {TABS.map((tab) => {
          const active = isTabActive(tab.path, location.pathname);
          return (
            <Link
              key={tab.path}
              to={tab.path}
              className={cn(
                "flex flex-col items-center gap-0.5 px-3 py-2 text-[10px] font-medium transition-colors",
                active ? "text-primary" : "text-foreground/40 hover:text-foreground/60",
              )}
              aria-current={active ? "page" : undefined}
            >
              {tab.icon}
              <span>{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
